module ArithmeticLogicUnit(
        input [31:0] A,
        input [31:0] B,
        input [5:0] Op,
        output reg [31:0] C,
        output reg Over
    );
    
    reg [31:0] mask, temp;
    // CC means the sum of two operands
    // DD means the diff of two operands
    wire [31:0] CC, DD, OOver, Cout;
    adder_32 adder(.a(A), .b(B), .sum(CC), .cin(1'b0), .cout(Cout));
    adder_32 minuser(.a(A), .b(~B+1), .sum(DD), .cin(1'b0), .cout(Cout));
    
    always @(*)
        begin
            Over = 0;
            case(Op)
            6'b100000:
                begin
                C <= CC;
                Over <= ~(A[31] ^ B[31]) & (A[31] ^ CC[31]);
                end
            6'b100001:
                C <= CC;
            6'b100010:
                begin
                C <= DD;
                Over <= (A[31] ^ B[31]) & (A[31] ^ DD[31]);
                end
            6'b100011:
                C <= DD;
            6'b000000:
                C <= B << A[4:0];
            6'b000010:
                C <= B >> A[4:0];
            6'b000011:
                begin
                if(B[31])
                    mask <= ~(32'b0) << 6'b100000 - A[4:0];
                else
                    mask <= 0;
                temp <= B >> A[4:0];
                C = temp | mask;
                end
            6'b100100:
                C <= A & B;
            6'b100101:
                C <= A | B;
            6'b100110:
                C <= A ^ B;
            6'b100111:
                C <= ~(A | B);
            default:
                C <= 0;
            endcase
        
        end
    
endmodule
